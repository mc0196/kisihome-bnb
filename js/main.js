let calendarOffset = 0;
let occupiedDates = new Set();

const APPS_SCRIPT_URL = 'https://script.google.com/macros/s/AKfycbzC2OccxXHNb_b-XRCwIKAawk3o0ySXhOdxDjT8n5Ygs-5_Y53OLxKynMITmNHGxgKM/exec';

async function loadAvailability() {
  try {
    const res = await fetch(APPS_SCRIPT_URL);
    if (!res.ok) throw new Error('Failed to load');
    const data = await res.json();
    occupiedDates = new Set(data.occupied || []);
  } catch (e) {
    console.warn('Availability data not loaded:', e.message);
  }
  renderCalendar();
}

function renderCalendar() {
  const container = document.getElementById('calendarMonths');
  container.innerHTML = '';

  const today = new Date();
  const base = new Date(today.getFullYear(), today.getMonth() + calendarOffset, 1);

  for (let i = 0; i < 2; i++) {
    const monthDate = new Date(base.getFullYear(), base.getMonth() + i, 1);
    container.appendChild(buildMonth(monthDate, today));
  }
}

function buildMonth(date, today) {
  const t = translations[currentLang];
  const MONTH_NAMES = t['cal.months'];
  const DAY_NAMES = t['cal.days'];

  const year = date.getFullYear();
  const month = date.getMonth();
  const firstDay = new Date(year, month, 1);
  const daysInMonth = new Date(year, month + 1, 0).getDate();
  const todayMidnight = new Date(today.getFullYear(), today.getMonth(), today.getDate());

  let startDow = firstDay.getDay() - 1;
  if (startDow < 0) startDow = 6;

  const monthEl = document.createElement('div');
  monthEl.className = 'calendar-month';

  const header = document.createElement('div');
  header.className = 'month-header';
  header.textContent = `${MONTH_NAMES[month]} ${year}`;
  monthEl.appendChild(header);

  const grid = document.createElement('div');
  grid.className = 'calendar-grid';

  DAY_NAMES.forEach(name => {
    const cell = document.createElement('div');
    cell.className = 'cal-day-name';
    cell.textContent = name;
    grid.appendChild(cell);
  });

  for (let i = 0; i < startDow; i++) {
    grid.appendChild(document.createElement('div'));
  }

  for (let d = 1; d <= daysInMonth; d++) {
    const cell = document.createElement('div');
    cell.className = 'cal-day';
    cell.textContent = d;

    const dateStr = `${year}-${String(month + 1).padStart(2, '0')}-${String(d).padStart(2, '0')}`;
    const thisDate = new Date(year, month, d);
    cell.dataset.date = dateStr;

    if (occupiedDates.has(dateStr)) {
      cell.classList.add('occupied');
      cell.title = t['cal.occupied'];
    } else if (thisDate < todayMidnight) {
      cell.classList.add('past');
    } else if (thisDate.getTime() === todayMidnight.getTime()) {
      cell.classList.add('today');
      cell.title = t['cal.today'];
      cell.addEventListener('click', () => toggleDate(dateStr));
      if (rangeStart && !rangeEnd) cell.addEventListener('mouseover', () => previewRange(dateStr));
    } else {
      cell.classList.add('available');
      cell.addEventListener('click', () => toggleDate(dateStr));
      if (rangeStart && !rangeEnd) cell.addEventListener('mouseover', () => previewRange(dateStr));
    }

    if (rangeStart && dateStr === rangeStart) cell.classList.add('selected');
    if (rangeEnd && dateStr === rangeEnd) cell.classList.add('selected');
    if (rangeStart && rangeEnd && dateStr > rangeStart && dateStr < rangeEnd && !occupiedDates.has(dateStr)) {
      cell.classList.add('in-range');
    }

    grid.appendChild(cell);
  }

  monthEl.appendChild(grid);
  return monthEl;
}

